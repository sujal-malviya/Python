# Arithmetic Operator
# add,sub,div,multiply,modulo .

x = int(input("Enter a number: "))
y = int(input("Enter a number: "))

sum = x+y
diff = x-y
prod = x*y
div = x/y
modulo = x%y

print(f"\nAddition of {x} + {y} = {sum}")
print(f"Subtraction of {x} - {y} = {diff}")
print(f"Multiplication of {x} * {y} = {prod}")
print(f"Division of {x} / {y} = {div}")
print(f"Modulus of {x} % {y} = {modulo}")
